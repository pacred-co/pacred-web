function TermBlock({ b }: { b: Block }) {
  if (b.t === "p")
    return (
      <p className="text-[13.5px] md:text-[15px] leading-[1.85] text-muted">{b.text}</p>
    );
  if (b.t === "h")
    return (
      <h4 className="pt-1.5 text-[13.5px] md:text-[15px] font-bold text-foreground">
        {b.text}
      </h4>
    );
  if (b.t === "list")
    return (
      <ul className="space-y-1.5">
        {b.items.map((it, i) => (
          <li key={i} className="flex gap-2.5 text-[13.5px] md:text-[15px] leading-[1.8] text-muted">
            <span className="mt-[9px] h-1.5 w-1.5 shrink-0 rounded-full bg-primary-400" />
            <span>{it}</span>
          </li>
        ))}
      </ul>
    );
  // note
  return (
    <div className="rounded-lg border border-amber-200 bg-amber-50 px-3.5 py-2.5 text-[12.5px] md:text-[13.5px] leading-[1.8] text-amber-800 dark:border-amber-900/40 dark:bg-amber-900/15 dark:text-amber-200">
      <span className="font-bold">หมายเหตุ: </span>
      {b.text}
    </div>
  );
}

